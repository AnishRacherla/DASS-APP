    lang = story["lang"]
    for i, text in enumerate(story["texts"]):
        if i == len(story["texts"]) - 1:
            filename = f"backend/assets/audio/storyTime/{prefix}_moral.mp3"
        else:
            filename = f"backend/assets/audio/storyTime/{prefix}_slide_{i+1}.mp3"
            
        if not os.path.exists(filename):
            print(f"Generating {filename}...")
            # Slower speech is better for kids
            gTTS(text=text, lang=lang, slow=False).save(filename)

